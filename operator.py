num1 = 5 #"=" is assignment operator
num2 = 10

sum = num1 + num2  # "+" is arithmetic operator
dif = num2 - num1
prod = num1 * num2
divide = num2 / num1
mod = num2 % num1 #modulus(%) operator gives remainder
quot = num2 // num1 #// gives us the quotient 
power = num1 ** 4 # "**" is used as power operator


#Arthematic operators
print("----------Arithmetic Operators----------")
print("Sum is ", sum)
print("Difference is ", dif)
print("Product is ", prod)
print("Quotient is ", quot)
print("Modulus is ", mod)
print("Power is ", power)
print("Floor Division is ", quot)


#conditional operators
print("----------Conditional Operators----------")
print(num1 == num2)
print(num1 != num2)
print(num1 > num2)
print(num1 < num2)
print(num1 >= num2)
print(num1 <= num2)


#Formatted string
print("----------Formatted String----------")
print(f"Num1 is {num1} and Num2 is {num2}") #f before string allows to put varaiable, using curly braces {}