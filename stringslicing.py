a = "My name is Computer"
b = "Computer"




print("--String Slicing Examples--")
print(b[0:5])
print(a[1:4])
print(a[3:10])
print(a[2:])
print(a[:10])
print(a[-5:-1])
print(a[-10:-3])
print(a[:-2])
print(a[-5:])


print(a[0:10:2]) #"2" skips 2 character after printing one character
print(a[-15:-1:3])

